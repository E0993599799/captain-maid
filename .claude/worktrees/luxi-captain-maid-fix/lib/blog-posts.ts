function extractExcerpt(markdown: string): string {
  // Take the first paragraph after the title (line after # title)
  const lines = markdown.split('\n');
  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed && !trimmed.startsWith('#') && !trimmed.startsWith('![') && !trimmed.startsWith('>')) {
      return trimmed.replace(/\*\*/g, '').replace(/\*/g, '').slice(0, 150) + '...';
    }
  }
  return '';
}

function estimateReadTime(content: string): string {
  const thaiChars = content.match(/[\u0E00-\u0E7F]/g)?.length || 0;
  const words = thaiChars / 3 + content.split(/\s+/).length;
  const minutes = Math.max(1, Math.ceil(words / 300));
  return `อ่าน ${minutes} นาที`;
}

